public class RedheadDuck extends Duck implements FlyBehavior, QuackBehavior {

    public void display() {
        System.out.println("looks like a redhead");
    }

    public void fly() {
        System.out.println("fly like a redhead");
    }

    public void quack() {
        System.out.println("quack like a redhead");
    }
}
